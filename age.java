public class age
{
    public static void main(String [] args)
    {
        int current = 21;
        int doubleAge = (current * 2);
        int tripleAge = (current * 3);

        System.out.println("My current age is: " + current + ".");
        System.out.println("Double my current age is: " + doubleAge + ".");
        System.out.println("Triple my current age is: " + tripleAge + ".");
    }
}